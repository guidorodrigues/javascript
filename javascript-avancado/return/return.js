// return retorna um valor de uma função
// return termina a execução da função

function soma(a, b) {
  return a + b; // retorna o resultado da soma
}
console.log(soma(5, 2)); // apenas exibe o valor 7

function soma2(a, b) {
  console.log(a + b); // imprime o resultado da soma // apenas exibe o valor
}

soma2(5, 2); // 7
